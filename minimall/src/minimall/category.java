    private void table_update()
    {
        try {
            int mini;
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/minimall","root","");
                prest = (PreparedStatement) con.prepareStatement("select * from category");
                ResultSet result = (ResultSet) prest.executeQuery();
                
                ResultSetMetaData set = (ResultSetMetaData) result.getMetaData();
                mini = set.getColumnCount();
                
                DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
                dtm.setRowCount(0);
               
                while(result.next())
                {
                    Vector vc = new Vector();
                    
                    for(int i=1; i<=mini; i++)
                    {
                        vc.add(result.getString("id"));
                        vc.add(result.getString("category"));
                        vc.add(result.getString("status"));
                    }
                    
                    dtm.addRow(vc);
                }
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(category.class.getName()).log(Level.SEVERE, null, ex);
            }
     
        } catch (SQLException ex) {
            Logger.getLogger(category.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
